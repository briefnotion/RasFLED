  RF_UNFILTERED_TTL.set_color(  color_range(sdSysData.CAR_INFO.CALCULATED.UNFILTHERED_RF_TTL.val_life_percentage_mean(), 0, 30, 50, 100, 200 ), COLOR_BLACK);
